    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        // Mengambil text dari masing-masing text field
        String id = txtIdKebudayaan.getText();
        String nama = txtNamaKebudayaan.getText();
        String deskripsi = txtDeskripsi.getText();
        String kategori = txtKategori.getText();
        
        // Validasi input - cek apakah ada field yang kosong
        if (id.isEmpty() || nama.isEmpty() || deskripsi.isEmpty() || kategori.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Semua field harus diisi!", 
                "Validasi Input", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return; // Keluar dari method jika ada field kosong
        }
        
        // Menambahkan data ke object DataKebudayaan
        obj.inputIdKebudayaan(id);
        obj.inputNamaKebudayaan(nama);
        obj.inputDeskripsi(deskripsi);
        obj.inputKategori(kategori);
        
        // Refresh tabel untuk menampilkan data baru
        tampilDataKebudayaan();
        
        // Clear semua text field setelah data ditambahkan
        txtIdKebudayaan.setText("");
        txtNamaKebudayaan.setText("");
        txtDeskripsi.setText("");
        txtKategori.setText("");
        
        // Set focus ke field ID untuk input berikutnya
        txtIdKebudayaan.requestFocus();
        
        // Menampilkan message bahwa data berhasil ditambahkan
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Data Kebudayaan berhasil ditambahkan!", 
            "Sukses", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnTambahActionPerformed

