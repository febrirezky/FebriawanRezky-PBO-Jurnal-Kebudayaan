    private void tampilDataKebudayaan() {
        // Membuat instance DefaultTableModel untuk manage tabel
        DefaultTableModel modelKolom = new DefaultTableModel();
        
        // Menambahkan column header ke table model
        modelKolom.addColumn("ID Kebudayaan");      // Kolom 1
        modelKolom.addColumn("Nama Kebudayaan");    // Kolom 2
        modelKolom.addColumn("Deskripsi");          // Kolom 3
        modelKolom.addColumn("Kategori");           // Kolom 4
        
        // Loop untuk menambahkan setiap baris data dari ArrayList
        for (int i = 0; i < obj.listDataId().size(); i++) {
            // Membuat array untuk satu baris data
            Object[] barisData = {
                obj.listDataId().get(i),           // ID dari index i
                obj.listDataNama().get(i),         // Nama dari index i
                obj.listDataDeskripsi().get(i),    // Deskripsi dari index i
                obj.listDataKategori().get(i)      // Kategori dari index i
            };
            
            // Menambahkan baris ke table model
            modelKolom.addRow(barisData);
        }
        
        // Set table model ke JTable untuk menampilkan data
        tabelKebudayaan.setModel(modelKolom);
    }
